import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const src = fs.readFileSync(
  path.join(root, 'public/deliverables/web-closer-intro/index.html'),
  'utf8'
);
const m = src.match(/<style>([\s\S]*?)<\/style>/);
if (!m) throw new Error('no style block');

const P = '.page-wrapper.template-navy_cyan_consult .nc-jh-deliverable';

function scopeAllSelectors(cssIn) {
  let result = '';
  let i = 0;
  while (i < cssIn.length) {
    while (i < cssIn.length && /\s/.test(cssIn[i])) {
      result += cssIn[i];
      i++;
    }
    if (i >= cssIn.length) break;
    if (cssIn[i] === '/' && cssIn[i + 1] === '*') {
      const end = cssIn.indexOf('*/', i);
      result += cssIn.slice(i, end + 2);
      i = end + 2;
      continue;
    }
    if (cssIn.slice(i, i + 6) === '@media') {
      const open = cssIn.indexOf('{', i);
      result += cssIn.slice(i, open + 1);
      i = open + 1;
      let depth = 1;
      const innerStart = i;
      while (i < cssIn.length && depth > 0) {
        if (cssIn[i] === '{') depth++;
        else if (cssIn[i] === '}') depth--;
        i++;
      }
      const inner = cssIn.slice(innerStart, i - 1);
      result += scopeAllSelectors(inner) + '}';
      continue;
    }
    const openBrace = cssIn.indexOf('{', i);
    if (openBrace === -1) break;
    const sel = cssIn.slice(i, openBrace).trim();
    let depth = 1;
    let j = openBrace + 1;
    while (j < cssIn.length && depth > 0) {
      if (cssIn[j] === '{') depth++;
      else if (cssIn[j] === '}') depth--;
      j++;
    }
    const body = cssIn.slice(openBrace + 1, j - 1);
    i = j;
    if (sel.startsWith('@')) {
      result += sel + '{' + body + '}';
      continue;
    }
    let scopedSel;
    if (sel === ':root') scopedSel = P;
    else if (sel === '*') scopedSel = `${P} *`;
    else if (sel === 'html') scopedSel = 'html';
    else if (sel === 'body') scopedSel = P;
    else {
      scopedSel = sel
        .split(',')
        .map((s) => `${P} ${s.trim()}`)
        .join(', ');
    }
    result += scopedSel + '{' + body + '}';
  }
  return result;
}

const finalCss = 'html { scroll-behavior: smooth; }\n' + scopeAllSelectors(m[1].trim());

const outPath = path.join(root, 'server', 'navyDeliverableScopedCss.js');
fs.writeFileSync(
  outPath,
  `/** AUTO-GENERATED by scripts/scope-navy-deliverable-css.mjs — do not edit by hand */\nexport const NAVY_DELIVERABLE_SCOPED_CSS = ${JSON.stringify(finalCss)};\n`
);
console.log('Wrote', outPath, 'length', finalCss.length);

const tsOut = path.join(root, 'src', 'lib', 'navyDeliverableScopedCss.ts');
fs.writeFileSync(
  tsOut,
  `/** AUTO-GENERATED by scripts/scope-navy-deliverable-css.mjs — do not edit by hand */\nexport const NAVY_DELIVERABLE_SCOPED_CSS: string = ${JSON.stringify(finalCss)};\n`
);
console.log('Wrote', tsOut);
